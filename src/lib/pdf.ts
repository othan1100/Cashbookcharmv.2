  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(90, 100, 115);
  doc.text(`Total transactions: ${transactions.length}`, 40, 148);

  doc.setFillColor(...(net >= 0 ? GREEN : RED));
  doc.roundedRect(w - 200, 100, 160, 60, 8, 8, "F");
  doc.setTextColor(220, 255, 230);
