  const cashIn = transactions.filter((t) => t.type === "in" || t.type === "credit").reduce((s, t) => s + Number(t.amount), 0);
  const cashOut = transactions.filter((t) => t.type === "out" || t.type === "debit").reduce((s, t) => s + Number(t.amount), 0);
  const net = cashIn - cashOut;

